
#include <stdio.h>
int main()
{
    int x=10, y=3; // Declare integer variables and assign value

    printf("Simple calculator \n");         // Print message

    printf("Addition : %d \n", x+y);        // Print sum
    printf("Subtraction : %d\n", x-y);      // Print difference
    printf("Multiplication : %d \n", x*y);   // Print product
    printf("Division :%d \n", x/y);         // Print quotient

    return 0;
}